package academit.java;

public class LoopFor {
    public static void main(String[] args) {
        for (int i = 0; i <= 100; ++i) {
            System.out.println(i + " ");
        }

        System.out.println();

        for (int i = 100; i >= 0; i = i - 5) {
            System.out.println(i + " ");
        }
    }
}
